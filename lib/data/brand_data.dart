  // Tüm markaları düz map olarak döner
  static Map<String, Map<String, String>> get allBrands {
    final Map<String, Map<String, String>> result = {};
    for (final brands in byCategory.values) {
      for (final brand in brands) {
        result[brand['id']!] = brand;
      }
    }
    return result;
  }

  // ID'ye göre marka getir
  static Map<String, String>? getBrand(String id) => allBrands[id];

  // Kategoriye göre marka listesi
  static List<Map<String, String>> getBrandsForCategory(String kategori) {
    return byCategory[kategori] ?? [];
  }
